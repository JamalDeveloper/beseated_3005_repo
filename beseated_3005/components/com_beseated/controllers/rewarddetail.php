/**
 * The Beseated Loyalty Controller
 *
 * @since  0.0.1
 */
class BeseatedControllerRewardDetail extends JControllerAdmin
{
	/**
	 * Constructor.
	 *
	 * @param   array  $config	An optional associative array of configuration settings.
	 * @return  ContentControllerArticles
	 * @see     JController
	 * @since   1.6
	 */
	public function __construct($config = array())
	{
		parent::__construct($config);
	}

	/**
	 * Proxy for getModel.
	 *
	 * @param   string  $name    The model name. Optional.
	 * @param   string  $prefix  The class prefix. Optional.
	 * @param   array   $config  Configuration array for model. Optional.
	 *
	 * @return  object  The model.
	 *
	 * @since   0.0.1
	 */
	public function getModel($name = 'RewardDetail', $prefix = 'BeseatedModel', $config = array('ignore_request' => true))
	{
		$model = parent::getModel($name, $prefix, $config);

		return $model;
	}

	public function bookreward()
	{
		$app        = JFactory::getApplication();
		$user       = JFactory::getUser();
		$rewardID   = $app->input->getInt('reward_id');
		$model      = $this->getModel();
		$bookReward = $model->bookreward($rewardID, $user->id);

		echo $bookReward;
		exit;
	}


}